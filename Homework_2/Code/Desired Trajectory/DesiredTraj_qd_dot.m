function qd_dot = DesiredTraj_qd_dot(t)
    qd1Dot = -0.125*sin(0.5*t) ...
         + 0.3926990816987*cos(1.5707963267949*t) ...
         - 0.3397852285574*sin(1.3591409142295*t) ...
         - 0.25*(1/sqrt(2))*sin((1/sqrt(2))*t);
    qd2Dot = -0.5*sin(t) ...
         + 0.7071067811865*cos(1.4142135623731*t) ...
         + 1.3591409142295*cos(exp(1)*t) ...
         - 1.5707963267949*sin(pi*t);
    qd_dot = [qd1Dot; qd2Dot];
end