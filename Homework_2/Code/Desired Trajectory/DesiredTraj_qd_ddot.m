function qd_ddot = DesiredTraj_qd_ddot(t)
    qd1DDot = -0.0625*cos(0.5*t) ...
         - 0.616850275068*sin(1.5707963267949*t) ...
         - 0.125*cos(0.7071067811865*t);
    qd2DDot = -0.5*cos(t) ...
         - 0.99999*sin(1.41421*t) ...
         - 3.69452*sin(exp(1)*t) ...
         - 4.93480*cos(pi*t);
    qd_ddot = [qd1DDot; qd2DDot];
end